#!/usr/bin/env node
'use strict';

/**
 * Builds src/js/data/radred_sav_data.js from Rad-Red-4.1-Team-Exporter.
 * Run from damage-calc root. Committed output allows builds without the sibling repo.
 */

const fs = require('fs');
const path = require('path');

const rootDir = path.join(__dirname, '..');
const outFile = path.join(rootDir, 'src', 'js', 'data', 'radred_sav_data.js');
const exporterDir = path.join(rootDir, '..', 'Rad-Red-4.1-Team-Exporter');
const pyFile = path.join(exporterDir, 'radred_team_extractor.py');
const speciesFile = path.join(exporterDir, 'Species.txt');
const movesFile = path.join(exporterDir, 'Moves.txt');
const itemsFile = path.join(exporterDir, 'Items.txt');
const csvFile = path.join(exporterDir, 'species_abilities.csv');

function needExporter() {
	return fs.existsSync(speciesFile) && fs.existsSync(pyFile);
}

function extractPyObject(py, marker, endNeedle) {
	const start = py.indexOf(marker);
	if (start === -1) throw new Error('Missing ' + marker + ' in radred_team_extractor.py');
	const brace = py.indexOf('{', start);
	const end = py.indexOf(endNeedle, brace);
	if (end === -1) throw new Error('Could not find end of ' + marker);
	return py.slice(brace, end + 1);
}

function loadCharacterMap(py) {
	const src = extractPyObject(py, 'character_map = ', '\ndef read_pokemon_party');
	// eslint-disable-next-line no-new-func
	return new Function('return ' + src)();
}

function loadSpeciesMovesItems() {
	const species = fs.readFileSync(speciesFile, 'utf8').split(/\r?\n/).filter(function (l) {
		return l.length > 0;
	});
	const moves = fs.readFileSync(movesFile, 'utf8').split(/\r?\n/).filter(function (l) {
		return l.length > 0;
	});
	const items = fs.readFileSync(itemsFile, 'utf8').split(/\r?\n/).filter(function (l) {
		return l.length > 0;
	});
	return { species: species, moves: moves, items: items };
}

function loadAbilityBySpecies() {
	const lines = fs.readFileSync(csvFile, 'utf8').trim().split(/\r?\n/);
	const header = lines.shift();
	if (!header || header.indexOf('Species') === -1) {
		throw new Error('Unexpected species_abilities.csv header');
	}
	const bySpecies = {};
	for (let i = 0; i < lines.length; i++) {
		const line = lines[i];
		const parts = line.split(',');
		if (parts.length < 4) continue;
		const species = parts[0];
		const primary = parts[1];
		const secondary = parts[2];
		const hidden = parts.slice(3).join(',').trim();
		bySpecies[species] = {
			primary: primary,
			secondary: secondary,
			hidden: hidden || null,
		};
	}
	return bySpecies;
}

function charMapToArray(map) {
	const arr = new Array(256);
	for (let b = 0; b < 256; b++) {
		var v = map[b];
		if (v === undefined) v = map['0x' + b.toString(16).toUpperCase().padStart(2, '0')];
		arr[b] = v !== undefined && v !== null ? String(v) : ' ';
	}
	return arr;
}

function main() {
	if (!needExporter()) {
		if (fs.existsSync(outFile)) {
			console.log('Rad-Red-4.1-Team-Exporter not found; keeping existing radred_sav_data.js');
			return;
		}
		console.error('Missing Rad-Red-4.1-Team-Exporter next to damage-calc and no radred_sav_data.js yet.');
		process.exit(1);
	}

	const py = fs.readFileSync(pyFile, 'utf8');
	const charMap = loadCharacterMap(py);
	const nicknameChars = charMapToArray(charMap);
	const data = loadSpeciesMovesItems();
	data.abilityBySpecies = loadAbilityBySpecies();
	data.nicknameChars = nicknameChars;

	const banner = '/* Auto-generated by scripts/generate-radred-sav-data.js — do not edit */\n';
	const body = 'window.RADRED_SAV_DATA = ' + JSON.stringify(data) + ';\n';
	fs.mkdirSync(path.dirname(outFile), { recursive: true });
	fs.writeFileSync(outFile, banner + body, 'utf8');
	console.log('Wrote ' + path.relative(rootDir, outFile));
}

main();
